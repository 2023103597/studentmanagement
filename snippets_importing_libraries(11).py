from langchain.agents import initialize_agent, Tool
from langchain.agents.agent_types import AgentType
from langchain.tools.python.tool import PythonREPLTool
from langchain.llms import OpenAI
import os

# ðŸ”‘ Set your OpenAI API key
os.environ["OPENAI_API_KEY"] = "your-openai-api-key"

# ðŸ”§ LLM (OpenAI)
llm = OpenAI(temperature=0)

# ðŸ”§ Tool: Python REPL (executes Python code)
tools = [
    Tool(
        name="Python REPL",
        func=PythonREPLTool().run,
        description="Useful for math, programming, and calculations"
    )
]

# ðŸ¤– Agent with LLM and REPL
agent = initialize_agent(
    tools,
    llm,
    agent=AgentType.ZERO_SHOT_REACT_DESCRIPTION,
    verbose=True
)

# ðŸ§ª Ask a question
question = "What is the derivative of x^2 + 3x + 2?"

response = agent.run(question)
print("\nðŸ”¹ Final Answer:\n", response)



